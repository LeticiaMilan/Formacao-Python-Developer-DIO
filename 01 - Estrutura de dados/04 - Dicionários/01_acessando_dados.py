dados = {"nome": "Letícia", "idade": 21, "telefone": "3333-1234"}

print(dados["nome"])  # "Letícia"
print(dados["idade"])  # 21
print(dados["telefone"])  # "3333-1234"

dados["nome"] = "João Vitor"
dados["idade"] = 22
dados["telefone"] = "9988-1781"

print(dados)  # {"nome": "João Vitor", "idade": 22, "telefone": "9988-1781"}